package com.model;

public class Site {
	private String clinicName;
	private String clinicAddress;
	private String doctorName;
	private String clinicId;
	private String clinicUrlName;
	private String clinicLogoUrl;
	private String doctorImageUrl;
	public String getClinicName() {
		return clinicName;
	}
	public void setClinicName(String clinicName) {
		this.clinicName = clinicName;
	}
	public String getClinicAddress() {
		return clinicAddress;
	}
	public void setClinicAddress(String clinicAddress) {
		this.clinicAddress = clinicAddress;
	}
	public String getDoctorName() {
		return doctorName;
	}
	public void setDoctorName(String doctorName) {
		this.doctorName = doctorName;
	}
	public String getClinicId() {
		return clinicId;
	}
	public void setClinicId(String clinicId) {
		this.clinicId = clinicId;
	}
	public String getClinicUrlName() {
		return clinicUrlName;
	}
	public void setClinicUrlName(String clinicUrlName) {
		this.clinicUrlName = clinicUrlName;
	}
	public String getClinicLogoUrl() {
		return clinicLogoUrl;
	}
	public void setClinicLogoUrl(String clinicLogoUrl) {
		this.clinicLogoUrl = clinicLogoUrl;
	}
	public String getDoctorImageUrl() {
		return doctorImageUrl;
	}
	public void setDoctorImageUrl(String doctorImageUrl) {
		this.doctorImageUrl = doctorImageUrl;
	}
	
	

}
